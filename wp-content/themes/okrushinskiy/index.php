                        <p style="text-align: justify; ">
                            - судебная практика (юридическая помощь в подготовке всех процессуальных документов: жалоб, исков, ходатайств и т.д.; регулирование корпоративных и трудовых споров; представление в судах интересов клиента (суды всех инстанций и уровней), обжалование решений государственной власти и учреждений; представление интересов на всех стадия судебного процесса);</p>
                        <p style="text-align: justify; ">
                            - уголовно-правовая практика (представление интересов компаний, частных лиц в уголовном судопроизводстве в экономическом и коррупционном направлении);</p>
